    private static String appendQuery(String uri, String appendQuery) {
        try {
            URI oldUri = new URI(uri);
            String newQuery = oldUri.getQuery();
            if (newQuery == null) {
                newQuery = appendQuery;
            } else {
                newQuery += "&" + appendQuery;
            }
            URI newUri = new URI(
                    oldUri.getScheme(),
                    oldUri.getAuthority(),
                    oldUri.getPath(), newQuery, oldUri.getFragment()
            );
            return newUri.toString();
        } catch (Exception e) {
            e.printStackTrace();
            return uri;
        }
    }

    public static String getHostName(String url) {
        try {
            URI uri = new URI(url);
            String new_url = uri.getHost();
            if (!new_url.startsWith("www.")) new_url = "www." + new_url;
            return new_url;
        } catch (URISyntaxException e) {
            if (e.getMessage() != null) {
                Log.e("ERROR", e.getMessage());
            }
            return url;
        }
    }

    public static boolean isStoragePermissionGranted(Activity activity) {
        if (Build.VERSION.SDK_INT >= 23) {
            if (activity.checkSelfPermission(android.Manifest.permission.WRITE_EXTERNAL_STORAGE)
                    == PackageManager.PERMISSION_GRANTED) {
                return true;
            } else {

                ActivityCompat.requestPermissions(activity, new String[]{Manifest.permission.WRITE_EXTERNAL_STORAGE}, 1);
                return false;
            }
        } else { //permission is automatically granted on sdk<23 upon installation
            return true;
        }
    }

    public static boolean isSDCardPresent() {
        return Environment.getExternalStorageState().equals(

                Environment.MEDIA_MOUNTED);
    }

    public static String createApkStorage(Context context) {
        File apkStorage = new File(
                Environment.getExternalStorageDirectory() + "/"
                        + "Love Rekindle");
        //Get File if SD card is present
        if (!isSDCardPresent())
            Toast.makeText(context, "Oops!! There is no SD Card.", Toast.LENGTH_SHORT).show();

        //If File is not present create directory
        if (!apkStorage.exists()) {
            apkStorage.mkdir();
            Log.e(context.getPackageName(), "Directory Created.");
        }

        return apkStorage.getPath();

    }

    public static void displayActionSnackBar(final Activity context, View parent_view, String title, String subTitle, String btnText, final View.OnClickListener action) {
        final Snackbar snackbar = Snackbar.make(parent_view, "", Snackbar.LENGTH_INDEFINITE);
        //inflate view
        View custom_view = context.getLayoutInflater().inflate(R.layout.snackbar_toast_floating, null);
        ((TextView) custom_view.findViewById(R.id.title)).setText(title);
        ((TextView) custom_view.findViewById(R.id.subtitle)).setText(subTitle);
        ((TextView) custom_view.findViewById(R.id.btn)).setText(btnText);

        snackbar.getView().setBackgroundColor(Color.TRANSPARENT);
        Snackbar.SnackbarLayout snackBarView = (Snackbar.SnackbarLayout) snackbar.getView();
        snackBarView.setPadding(0, 0, 0, 0);
        (custom_view.findViewById(R.id.btn)).setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                snackbar.dismiss();
                action.onClick(v);
            }
        });

        snackBarView.addView(custom_view, 0);
        snackbar.show();
    }


